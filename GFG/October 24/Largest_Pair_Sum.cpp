class Solution {
  public:
    int pairsum(vector<int> &arr) {
        // code here
        int max_el = arr[0];
        int max_sum = 0;
        
        for(int i=1;i<arr.size();i++){
            max_sum = max(max_sum,max_el+arr[i]);
            max_el = max(max_el,arr[i]);
        }
        
        return max_sum;
    }
};


